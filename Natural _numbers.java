import java.util.*;
class Reverse_number
  {
    public static void main(String args[])
    {
      int i,n;
      Scanner sc=new Scanner(System.in);
      System.out.println("Enter reverse number");
      n=sc.nextInt();
      {
        for(i=n;i>=1;i--)
          {
            System.out.println(i);
          }
      }
    }
  }